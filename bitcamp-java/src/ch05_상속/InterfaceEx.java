package ch05_상속;
interface PhoneInterface{ // 인터페이스 선언
	final int TIMEOUT = 10000; // 상수 필드 선언
	void sendCall(); // 추상 메소드
	void receiveCall(); // 추상 메소드
	default void printLogo() { // default 메소드
		System.out.println("**phone**");
	}
}

interface MobilePhoneInterface extends PhoneInterface{
	void sendSMS();
	void receiveSMS();
}

interface MP3Interface{ // 인터페이스 선언
	public void play();
	public void stop();
}
class PDA{
	public int calculate(int x, int y) {
		return x+y;
	}
}

// SmartPhone 클래스는 PDA 상속받고,
// MobilePhoneInterface와 MP3Interface 인터페이스에 선언된 추상 메소드를 모두 구현
class SmartPhone extends PDA implements MobilePhoneInterface, MP3Interface{
	// MobilePhoneInterface의 추상 메소드 구현
	
	@Override
	public void sendCall() {
		System.out.println("따르릉따르릉~~");
	}

	@Override
	public void receiveCall() {
		System.out.println("전화 왔어요.");
	}

	@Override
	public void play() {
		System.out.println("음악 연주합니다.");
	}

	@Override
	public void stop() {
		System.out.println("음악 중단합니다.");
	}

	@Override
	public void sendSMS() {
		System.out.println("문자갑니다.");
	}

	@Override
	public void receiveSMS() {
		System.out.println("문자 왔어요.");
	}
	
	public void schedule() {
		System.out.println("일정 관리합니다.");
	}
	
}
class SamsungPhone implements PhoneInterface{ // 인터페이스 구현
	//PhoneInterface의 모든 추상 메소드 구현
		
	@Override
	public void sendCall() {
		System.out.println("띠리리리링");
	}

	@Override
	public void receiveCall() {
		System.out.println("전화가 왔습니다.");
	} 
	
	public void flash() {
		System.out.println("전화기에 불이 켜졌습니다.");
	}
}
public class InterfaceEx {

	public static void main(String[] args) {

		SamsungPhone phone = new SamsungPhone();
		phone.printLogo();
		phone.sendCall();
		phone.receiveCall();
		phone.flash();
		System.out.println();
		
		SmartPhone phone2 = new SmartPhone();
		phone2.printLogo();
		phone2.sendCall();
		phone2.play();
		System.out.println("3과 5를 더하면 " + phone2.calculate(3, 5));
		phone2.schedule();
	}

}
